package classes;

public class Servicio {

	private String name,
				   payDay,
				   accountNo,
				   bank;
	
	private double amount;
	
	public Servicio(String name, String payDay, String accountNo, String bank, double amount) {
		
		this.name = name;
		this.payDay = payDay;
		this.accountNo = accountNo;
		this.bank = bank;
		this.amount = amount;
	}
	
}
